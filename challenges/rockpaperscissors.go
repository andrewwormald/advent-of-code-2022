package challenges

import (
	"strings"
)

type Type int

const (
	Unknown  Type = 0
	Rock     Type = 1
	Paper    Type = 2
	Scissors Type = 3
)

func (t Type) Score() int {
	switch t {
	case Rock:
		return 1
	case Paper:
		return 2
	case Scissors:
		return 3
	default:
		return 0
	}
}

type Outcome int

const (
	UnknownOutcome Outcome = 1
	Loss           Outcome = 1
	Draw           Outcome = 2
	Win            Outcome = 3
)

func ParseOutcome(s string) Outcome {
	switch s {
	case "X":
		return Loss
	case "Y":
		return Draw
	case "Z":
		return Win
	default:
		return UnknownOutcome
	}
}

func (o Outcome) Score() int {
	switch o {
	case Draw:
		return 3
	case Loss:
		return 0
	case Win:
		return 6
	default:
		return 0
	}
}

func parseOpponent(s string) Type {
	switch s {
	case "A":
		return Rock
	case "B":
		return Paper
	case "C":
		return Scissors
	default:
		return Unknown
	}
}

func parseMe(s string) Type {
	switch s {
	case "X":
		return Rock
	case "Y":
		return Paper
	case "Z":
		return Scissors
	default:
		return Unknown
	}
}

// determine compares A to be and will return the result respective to a in the following manner:
func determine(a, b Type) Outcome {
	if a == b {
		return Draw
	}

	if a == Rock {
		if b == Paper {
			return Loss
		}

		if b == Scissors {
			return Win
		}
	}

	if a == Paper {
		if b == Scissors {
			return Loss
		}

		if b == Rock {
			return Win
		}
	}

	if a == Scissors {
		if b == Rock {
			return Loss
		}

		if b == Paper {
			return Win
		}
	}

	return UnknownOutcome
}

func determineFromOutcome(t Type, o Outcome) Type {
	if o == Draw {
		return t
	}

	if t == Rock {
		if o == Loss {
			return Scissors
		}

		if o == Win {
			return Paper
		}
	}

	if t == Paper {
		if o == Loss {
			return Rock
		}

		if o == Win {
			return Scissors
		}
	}

	if t == Scissors {
		if o == Loss {
			return Paper
		}

		if o == Win {
			return Rock
		}
	}

	return Unknown
}

func RockPaperScissorsScore(input string) int {
	rounds := strings.Split(input, "\n")
	total := 0
	for _, round := range rounds {
		if round == "" {
			continue
		}

		entries := strings.Split(round, " ")
		opponent := parseOpponent(entries[0])
		outcome := ParseOutcome(entries[1])
		me := determineFromOutcome(opponent, outcome)

		total += outcome.Score() + me.Score()
	}

	return total
}

